import java.util.*;
public class Comparision
{
public static void main(String[] args)
{
    int a=45;
    int b=65;
    System.out.println(a=b);
    System.out.println(a!=b);
    System.out.println(a<=b);
    System.out.println(a<b);
    System.out.println(a>=b);
    System.out.println(a>b);
}
}