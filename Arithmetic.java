import java.util.*;
public class Arithmetic
{
    public static void main(String[] args)
    {
        int a=23;
        int b=34;
        System.out.println("a+b="+a+b);
        System.out.println("a*b="+a*b);
        System.out.println("a/b="+a/b);
        System.out.println("a%b="+a%b);
    }
}