import java.util.*;
public class Assignment
{
   public static void main(String[] args)
   {
      int a=20;
      int b=23;
      System.out.println(a=b);
      System.out.println(a+=b);
      System.out.println(a-=b);
      System.out.println(a*=b);
      System.out.println(a/=b);
      System.out.println(a%=b);
      System.out.println(a++);
      System.out.println(++a);
      System.out.println(b--);
      System.out.println(--b); 
   }
}